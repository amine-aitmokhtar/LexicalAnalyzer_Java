public enum TokenType {
    // single-character tokens
    OPEN_PAREN, CLOSE_PAREN, COMMA, SEMICOLON,

    // keywords
    PROGRAM, VAR, FUNCTION, BEGIN, END, IF, THEN, ELSE, RETURN,
    
  
    
    //types
    INT,CONSTANTE,

    // identifiers
   IDENTIFIER, MOT_CLES, NUMBER,n,fib,i,j,

    // Caractere Special
   CARACTERE_SPECIAL, ASSIGNMENT, PLUS, MINUS,EQUAL,

    // end of file
    EOF, TYPE,
}


